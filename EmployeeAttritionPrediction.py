def domain_driven_outliers(df_input):
    """
    Function to clean outliers based on HR business logic.
    Removes data that is operationally illogical.
    NOTE: Conditions below are adjusted for data scaled typically between 0-4.
    You may need to fine-tune these based on your specific scaling logic.
    """
    initial_count = df_input.shape[0]

    # Adjusted conditions for scaled data (assuming 0-4 range as observed from df.describe())
    # The 'MonthlySalary > 0.1' condition is commented out as it was too aggressive on scaled data where 0.0 is a valid value.
    df_filtered = df_input[
        (df_input['Age'] >= 0.1) & (df_input['Age'] <= 3.9) &                     # Adjusted for scaled Age (e.g., not 0 or 4)
        (df_input['Years_Experience'] >= 0) & (df_input['Years_Experience'] <= 4) & # Allow standard scaled range
        (df_input['Distance_to_work'] >= 0) & (df_input['Distance_to_work'] <= 3.9) & # Adjusted for scaled Distance
        #(df_input['MonthlySalary'] > 0.1) &                                # Assuming 0 is a placeholder, check for actual value - COMMENTED OUT TO PREVENT EMPTY DATAFRAME
        (df_input['Years_experience_lastorganization'] <= df_input['Years_Experience']) # Relative comparison, likely fine
    ].copy()

    dropped_count = initial_count - df_filtered.shape[0]
    print(f"--- Domain-Driven Outlier Detection ---")
    print(f"Baris dihapus : {dropped_count}")
    print(f"Sisa data     : {df_filtered.shape[0]}")

    return df_filtered

